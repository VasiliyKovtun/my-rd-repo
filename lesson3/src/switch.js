
const s = 'true';

switch (s) {
    case 'false':
        console.log(`${s} is not true`);
        break;
    case 'null':
        console.log(`${s} is not true`);
        break;
    case 'undefined':
        console.log(`${s} is not true`);
        break;
    default:
        console.log(`${s} is true`);
        break;
};
